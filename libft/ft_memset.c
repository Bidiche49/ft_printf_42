#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	unsigned char	*tmp;

	tmp = (unsigned char *)s;
	while ((size_t)(tmp - (unsigned char *)s) < n)
		*tmp++ = c;
	return (s);
}
